package ru.java_core.level14;

import java.io.BufferedReader;
import java.io.FileReader;

public class Task1404 {
    public static void main(String[] args) {
        final String fileTest = "src\\main\\resources\\java_test";
        try (final BufferedReader reader = new BufferedReader(new FileReader(fileTest))) {
            String key;
            while (!(key = reader.readLine()).equals("")) {
                System.out.println(CatFactory.getCatByKey(key).toString());
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    static class CatFactory {
        static Cat getCatByKey(String key) {
            Cat cat;
            switch (key) {
                case "vaska":
                    cat = new MaleCat("Василий");
                    break;
                case "murka":
                    cat = new FemaleCat("Мурочка");
                    break;
                case "kiska":
                    cat = new FemaleCat("Кисюлька");
                    break;
                default:
                    cat = new Cat(key);
                    break;
            }
            return cat;
        }
    }

    static class Cat {
        private String name;

        protected Cat(String name) {
            this.name = name;
        }

        public String getName() {
            return this.name;
        }

        public String toString() {
            return "Я уличный кот " + getName();
        }
    }

    static class MaleCat extends Cat {
        MaleCat(String name) {
            super(name);
        }

        public String toString() {
            return "Я - солидный кошак по имени " + getName();
        }
    }

    static class FemaleCat extends Cat {
        FemaleCat(String name) {
            super(name);
        }

        public String toString() {
            return "Я - милая кошечка по имени " + getName();
        }
    }
}
